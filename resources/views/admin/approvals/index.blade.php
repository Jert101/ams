@push('scripts')
<script>
    document.addEventListener('DOMContentLoaded', function() {
        // Rejection modal functionality
        const rejectionModal = document.getElementById('rejection-modal');
        const rejectionForm = document.getElementById('rejection-form');
        const cancelRejection = document.getElementById('cancel-rejection');
        const rejectButtons = document.querySelectorAll('.reject-button');
        
        // Success modal functionality
        const successModal = document.getElementById('success-modal');
        const closeSuccessModal = document.getElementById('close-success-modal');
        const successModalTitle = document.getElementById('success-modal-title');
        const successModalMessage = document.getElementById('success-modal-message');
        
        // Approve forms
        const approveForms = document.querySelectorAll('.approve-form');
        
        // Show rejection modal
        rejectButtons.forEach(button => {
            button.addEventListener('click', function() {
                const userId = this.getAttribute('data-user-id');
                rejectionForm.action = `/admin/approvals/${userId}/reject`;
                rejectionModal.classList.remove('hidden');
            });
        });
        
        // Hide rejection modal
        cancelRejection.addEventListener('click', function() {
            rejectionModal.classList.add('hidden');
        });
        
        // Handle rejection form submission
        rejectionForm.addEventListener('submit', function(e) {
            e.preventDefault();
            
            const formData = new FormData(rejectionForm);
            
            fetch(rejectionForm.action, {
                method: 'POST',
                body: formData,
                headers: {
                    'X-Requested-With': 'XMLHttpRequest',
                    'Accept': 'application/json',
                },
                credentials: 'same-origin'
            })
            .then(response => response.json())
            .then(data => {
                rejectionModal.classList.add('hidden');
                
                // Show success modal
                successModalTitle.textContent = 'Registration Rejected';
                successModalMessage.textContent = 'The user registration has been rejected successfully.';
                successModal.classList.remove('hidden');
                
                // Remove the user row from the table
                const userId = rejectionForm.action.split('/').pop();
                const userRow = document.getElementById(`user-row-${userId}`);
                if (userRow) {
                    userRow.remove();
                }
                
                // Check if there are no more pending users
                const tbody = document.querySelector('tbody');
                if (tbody && tbody.children.length === 0) {
                    location.reload(); // Reload to show the "No pending registrations" message
                }
            })
            .catch(error => {
                console.error('Error:', error);
            });
        });
        
        // Handle approve form submissions
        approveForms.forEach(form => {
            form.addEventListener('submit', function(e) {
                e.preventDefault();
                
                const userRow = this.closest('tr');
                const userId = userRow.id.replace('user-row-', '');
                
                fetch(this.action, {
                    method: 'POST',
                    body: new FormData(this),
                    headers: {
                        'X-Requested-With': 'XMLHttpRequest',
                        'Accept': 'application/json'
                    }
                })
                .then(response => response.json())
                .then(data => {
                    // Show success modal
                    successModalTitle.textContent = 'Registration Approved';
                    successModalMessage.textContent = 'The user registration has been approved successfully.';
                    successModal.classList.remove('hidden');
                    
                    // Remove the user row from the table
                    if (userRow) {
                        userRow.remove();
                    }
                    
                    // Check if there are no more pending users
                    const tbody = document.querySelector('tbody');
                    if (tbody && tbody.children.length === 0) {
                        location.reload(); // Reload to show the "No pending registrations" message
                    }
                })
                .catch(error => {
                    console.error('Error:', error);
                });
            });
        });
        
        // Close success modal
        closeSuccessModal.addEventListener('click', function() {
            successModal.classList.add('hidden');
        });
    });
</script>
